for i in range(5):  # range(n) goes from 0 up to n - 1
    print(i)

print("End of range(5)")

# range(start, stop) includes start but NOT stop
for i in range(3, 8):
    print(i)

print("End of range(3, 8)")

for i in range(5, 11):
    print(i)


print("End of range(5, 11)")

nums = list(range(1, 6))
print(nums)