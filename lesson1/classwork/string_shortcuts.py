first = "Pineapple"
last = "Pizza"

# String concatenation
full = first + " " + last
print(full)

# Repeat a string
print("ha" * 3)

# 'in' checks if something appears inside a string
sentence = "I like python"
print("python" in sentence)
print("java" in sentence)

# Escape characters
print("He said, \"hello\".")
print("Line 1\nLine 2")